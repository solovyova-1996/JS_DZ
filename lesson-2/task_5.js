// 5. Реализовать основные 4 арифметические операции в виде функций с двумя параметрами. 
// Обязательно использовать оператор return.

function getSumm(a, b) {
    return a + b
}

function getDifference(a, b) {
    return a - b
}

function getOp(a, b) {
    return a * b
}
function getDivisionRes(a, b) {
    return a / b
}

